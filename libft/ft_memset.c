#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	i;

	i = 0;
	if (!b && !len)
		return (NULL);
	while (i < len)
	{
		((unsigned char *)b)[i++] = c;
	}
	return (b);
}
